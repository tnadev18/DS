# Convert 'Pclass' to categorical variable
df['Pclass'] = df['Pclass'].astype('category')

# Convert 'Age' to numeric (assuming it's stored as object/string)
df['Age'] = pd.to_numeric(df['Age'], errors='coerce')

# Convert 'Age' to integer
