# Step 3: Perform one-hot encoding
# Let's say we want to encode the 'Sex' and 'Embarked' columns
# We'll use pd.get_dummies() to create binary columns for each category

# Perform one-hot encoding for 'Sex' column
sex_dummies = pd.get_dummies(df['Sex'], prefix='Sex')

# Perform one-hot encoding for 'Embarked' column
embarked_dummies = pd.get_dummies(df['Embarked'], prefix='Embarked')

# Concatenate the original DataFrame with the one-hot encoded columns
df_encoded = pd.concat([df, sex_dummies, embarked_dummies], axis=1)

# Drop the original categorical columns from the DataFrame
df_encoded.drop(['Sex', 'Embarked'], axis=1, inplace=True)

# Now, 'Sex' and 'Embarked' have been transformed into quantitative variables using one-hot encoding
# The new columns represent each category with binary values (0 or 1)

